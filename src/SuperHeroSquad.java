/**
 * This class represents a superhero squad with various abilities.
 * It includes methods to manage and display superhero information.
 */
public class SuperHeroSquad {

    // Method to display a greeting message
    public void displayGreeting() {
        System.out.println("Welcome to the SuperHero Squad!");
    }

    // Main method to run the program
    public static void main(String[] args) {
        // Create an instance of SuperHeroSquad
        SuperHeroSquad squad = new SuperHeroSquad();
        // Call the displayGreeting method
        squad.displayGreeting();
    }
}
